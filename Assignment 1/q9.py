
a = bool(int(input("Enter the first boolean value (0 or 1): ")))
b = bool(int(input("Enter the second boolean value (0 or 1): ")))


and_result = a and b
or_result = a or b
not_a = not a
not_b = not b


print(f"AND: {and_result}")
print(f"OR: {or_result}")
print(f"NOT A: {not_a}")
print(f"NOT B: {not_b}")
