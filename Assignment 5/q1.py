# Define the tuples
tuple1 = (5, 10, 15, "Computer Science", [4.1, 5.2])
tuple2 = (2, 4, 6, "Python", [1, 3, 5])

# a) Print 3rd element of tuple1
print("3rd element of tuple1:", tuple1[2])

# b) Print 3rd element of tuple2 using -ve indexing
print("3rd element of tuple2 using -ve indexing:", tuple2[-3])

# c) Print the item 5.2
print("Item 5.2:", tuple1[4][1])

# d) Define tuple3
tuple3 = ('p', 'y', 't', 'h', 'o', 'n', 'p', 'r', 'o', 'g', 'r', 'a', 'm', 'm', 'i', 'n', 'g')

# e) Print elements 4 to 8
print("Elements 4 to 8 of tuple3:", tuple3[3:8])

# f) Print elements 7 to end
print("Elements 7 to end of tuple3:", tuple3[6:])

# g) Print the elements from start to end
print("Elements from start to end of tuple3:", tuple3[:])
