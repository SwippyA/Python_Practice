# Create a list
list1 = [20, 22, 24, 26, 28, 30, ['apple', 'banana', 'mango', 'grapes', 'orange'], 3.14, 2.71, "hello", "world"]

# a) Print the list
print("List:", list1)

# b) Print the last item of the list
print("Last item of the list:", list1[-1])

# c) Print the second fruit name from the nested list
print("Second fruit name from nested list:", list1[6][1])

# d) Print the 4th item using Negative Indexing
print("4th item using negative indexing:", list1[-8])

# e) Print first two items of the list
print("First two items of the list:", list1[:2])

# f) Print the items from 3 to end
print("Items from 3 to end:", list1[2:])
